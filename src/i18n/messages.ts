type MessageTree = (typeof messages)[AppLocale];

function readPath(tree: MessageTree, key: string): string | undefined {
  const result = key
    .split(".")
    .reduce<unknown>((acc, part) => (acc && typeof acc === "object" ? (acc as Record<string, unknown>)[part] : undefined), tree);

  return typeof result === "string" ? result : undefined;
}

function interpolate(template: string, vars?: Record<string, string | number>): string {
  if (!vars) {
    return template;
  }

  return Object.entries(vars).reduce((value, [key, replacement]) => {
    return value.replaceAll(`{${key}}`, String(replacement));
  }, template);
}

export function t(
  locale: AppLocale,
  key: string,
  vars?: Record<string, string | number>,
): string {
  const localized = readPath(messages[locale], key);
  if (localized) {
    return interpolate(localized, vars);
  }

  const fallback = readPath(messages[DEFAULT_LOCALE], key);
  if (fallback) {
    return interpolate(fallback, vars);
  }

  return `[missing:${key}]`;
}

export function getLocaleMessages(locale: AppLocale): MessageTree {
  return messages[locale];
}
